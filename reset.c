Menu resetMenu;

#define options 5
typedef string strArray[options];
/**
 * The menus constructor
*/
void initResetMenu(strArray itemNames, strArray infos){
	resetMenu.itemCount = options;
	resetMenu.selected = 0;
	for(int i = 0; i < options; i++){
		resetMenu.itemNames[i] = itemNames[i];
		resetMenu.infos[i] = infos[i];
	}
}

/**
	workaround to robot c not having function pointers
*/
void activateResetMenu(int f){
	switch(resetMenu.selected){
		case 0: //lift
			switch(f){
				default:
					motor[LiftMtr] = 0;
					break;
				case 0:
					motor[LiftMtr]=-100;
					break;
				case 1:
					motor[LiftMtr]=100;
					break;
			}
			break;

		case 1: //flag
			switch(f){default:
					motor[FlagMtr] = 0;
					break;
				case 0:
					motor[FlagMtr]=-100;
					break;
				case 1:
					motor[FlagMtr]=100;
					break;
			}
			break;

		case 2: //turbofan
			switch(f){
				default:
					break;
				case 0:
					servo[Turbofan] += 1;
					break;
				case 1:
					servo[Turbofan] -= 1;
					break;

			}
			break;

		case 3:
			switch(f){
				default:
					motor[Intake] = 0;
				break;
				case 0:
					motor[Intake] = 100;
					break;
				case 1:
					motor[Intake] = -100;
					break;
			}
			break;
		case 4:
			switch(f){
				case 0:
					servo[RightIntake] = 255;
					servo[LeftIntake] = 0;
					break;
				case 1:
					servo[RightIntake] = 127 - intakeOffset;
					servo[LeftIntake] = 127 + intakeOffset;
			}
	}
}

task main()
{
	string itemNames[options] = {"Lift", "Flag", "Turbofan", "Intake", "Flip Out"};
	string infos[options] = {"", "", "", "", ""};
	initResetMenu(itemNames, infos);
	clearScreen();
	while(true){
		displayMenu(resetMenu);
		if(pressed(orangebutton)){
			selectNext(resetMenu);
		}

		if(held(leftarrow, 1)){
			activateResetMenu(0);
		}

		else if(held(rightarrow, 1)){
			activateResetMenu(1);
		}
		else {
			activateResetMenu(-1);
		}

		updateButtons();
	}
}
