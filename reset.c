Menu resetMenu;

#define options 4
typedef string strArray[options];
/**
 * The menus constructor
*/
void initResetMenu(strArray itemNames, strArray infos){
	resetMenu.itemCount = options;
	resetMenu.selected = 0;
	for(int i = 0; i < options; i++){
		resetMenu.itemNames[i] = itemNames[i];
		resetMenu.infos[i] = infos[i];
	}
}

/**
	workaround to robot c not having function pointers
*/
void activateResetMenu(int f){
	switch(resetMenu.selected){
		case 0: //lift
			switch(f){
				default:
					motor[LiftFlagMtr] = 0;
					break;
				case 0:
					motor[LiftFlagMtr]=-100;
					break;
				case 1:
					motor[LiftFlagMtr]=100;
					break;
			}
			break;

		case 1: //hopper
			switch(f){default:
					motor[PaddleMtr] = 0;
					break;
				case 0:
					motor[PaddleMtr]=-20;
					break;
				case 1:
					motor[PaddleMtr]=20;
					break;
			}
			break;

		case 2: //intake
			switch(f){
				default:
					servo[LeftIntake] = intakestop;
					servo[RightIntake] = intakestop;
					break;
				case 0:
					servo[LeftIntake] = -255;
					servo[RightIntake] = 255;
					break;
				case 1:
					servo[LeftIntake] = 255;
					servo[RightIntake] = -255;
					break;

			}
			break;

		case 3:
			if(f == -1){
				stopRobot();
				break;
			}
			motor[PaddleMtr] = -20;
			//motor[LiftFlagMtr] = -50;
			servo[LeftIntake] = -255;
			servo[RightIntake] = -255;
			wait1Msec(1000);
			motor[PaddleMtr] = 20;
			//motor[LiftFlagMtr] = 50;
			servo[LeftIntake] = 255;
			servo[RightIntake] = 255;
			wait1Msec(1000);
			stopRobot();
			break;
			case 4: //IR
			switch(f){default:
				case 0:
					clearScreen();
					nxtDisplayCenteredTextLine(2, "%d", SensorValue[AutoIR]);
					break;
				case 1:
					clearScreen();
					nxtDisplayCenteredTextLine(2, "%d", SensorValue[AutoIR]);
					break;
			}
	}
}

task main()
{
	string itemNames[options] = {"Lift", "Hopper", "Intake", "IR"};
	string infos[options] = {"", "", "", ""};
	initResetMenu(itemNames, infos);
	clearScreen();
	while(true){
		displayMenu(resetMenu);
		if(pressed(orangebutton)){
			selectNext(resetMenu);
		}

		if(held(leftarrow, 1)){
			activateResetMenu(0);
		}

		else if(held(rightarrow, 1)){
			activateResetMenu(1);
		}
		else {
			activateResetMenu(-1);
		}

		updateButtons();
	}
}
