Menu autoChooser;
/**
* The menus constructor
*/
void initautoChooser(strArray itemNames, strArray infos){
	autoChooser.itemCount = options;
	autoChooser.selected = 0;
	for(int i = 0; i < options; i++){
		autoChooser.itemNames[i] = itemNames[i];
		autoChooser.infos[i] = infos[i];
	}
}

/**
workaround to robot c not having function pointers
*/
void activateautoChooser(int f){
	switch(autoChooser.selected){
	case 0:
		switch (f){
		case 0:
			numberOfGoals--;
			break;
		case 1:
			numberOfGoals++;
			break;
		}
		numberOfGoals %= 5;
		if(numberOfGoals < 0){
			numberOfGoals += 5;
		}
		//*autoChooser.infos = numberOfGoals+1;
		StringFormat(autoChooser.infos[0], "%d", numberOfGoals+1);
		break;
	case 1:
		forwardBackward = !forwardBackward;
		autoChooser.infos[1] = forwardBackward ? "Forward" : "Backward";
		break;
	case 2:
		leftRight = !leftRight;
		autoChooser.infos[2] = leftRight ? "Forward" : "Backward";
		break;
	case 3:
		switch (f){
		case 0:
			bridgeSpot--;
			break;
		case 1:
			bridgeSpot++;
			break;
		case 2:
			bridgeSpot -= 3;
			break;
		case 3:
			bridgeSpot += 3;
			break;
		}
		numberOfGoals %= nBS;
		if(numberOfGoals < 0){
			numberOfGoals += nBS;
		}
		StringFormat(autoChooser.infos[3], "%d", bridgeSpot+1);
		break;
	case 4:
		switch (f){
		case 0:
			waittime--;
			break;
		case 1:
			waittime++;
			break;
		case 2:
			waittime -= 10;
			break;
		case 3:
			waittime += 10;
			break;
		}
		StringFormat(autoChooser.infos[4], "%d", waittime);
		break;
	}
}

