float thresholdify(float a){
    if(a > threshold){
        return (a-threshold)/(1-threshold);
    }
    else if(a < -threshold){
        return (a+threshold)/(1-threshold);
    }
    return 0;
}

float deadzone(float a){
    if(a > 127.0*threshold){
        return (a-127.0*threshold)/(1.0-threshold);
    }
    else if(a < -127.0*threshold){
        return (a+127.0*threshold)/(1.0-threshold);
    }
    return 0;
}

